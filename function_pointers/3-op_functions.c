/**
*op_add - Sums two integers.
*@a: First integer
*@b: Second integer
*
*Return: Sum of integers.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - Subtracts two integers.
*@a : First integer
*@b: Second integer
*
*Return: Subtraction of integers.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - Multiplies two integers.
*@a: First integer
*@b: Second integer
*
*Return: Multiplication of integers.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
*op_div - Divides two integers.
*@a: First integer
*@b: Second integer
*
*Return: Division of integer
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - Calculates remainder of two integers
*@a: First integer
*@b: Second integer
*
*Return: Modulus of integers.
*/
int op_mod(int a, int b)
{
	return (a % b);
}

